// create function that adds
function addition(x, y) {
    return x + y;
}

// create function that subtracts
function subtraction(x, y) {
    return x - y;
}

// create a function that multiplies
function multiply(x, y) {
    return x * y;
}

// create a function that divides
function divide(x, y) {
    return x / y;
}